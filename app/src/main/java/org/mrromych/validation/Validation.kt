package org.mrromych.validation

import android.widget.Toast

object Validation {
    fun validateRegistration(username: String, email: String, password: String): Boolean {
        var isValid = true
        val regexEmail = """(?:[a-z0-9!#${'$'}%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#${'$'}%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)])""".toRegex()
        val regexPassword = """^(?=.*[A-Za-z])(?=.*\d)(?=.*[@${'$'}!%*#?&])[A-Za-z\d@${'$'}!%*#?&]{8,}${'$'}""".toRegex()

        if(password.isBlank() || username.isBlank() || email.isBlank()) {
            isValid = false
        }

        if(!email.matches(regexEmail)) {
            isValid = false
        }

        if (username.length < 3) {
            isValid = false
        }

        if(!password.matches(regexPassword)) {
            isValid = false
        }

        return isValid;
    }

}